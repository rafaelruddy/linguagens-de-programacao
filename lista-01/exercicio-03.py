# 3. Média de Três Números
# Crie um programa que receba três números do usuário e calcule a média aritmética deles.
# A fórmula é:


num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
num3 = float(input("Digite o terceiro número: "))

media = (num1 + num2 + num3) / 3

print("Média: ", media)

